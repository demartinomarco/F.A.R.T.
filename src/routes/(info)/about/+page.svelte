	<header class="space-y-3">
		<p>
			F.A.R.T. (Finally A Reliable Timetable) ist eine schnelle, zuverlässige und
			benutzerfreundliche Anzeige für
			<strong>Echtzeit-Abfahrten</strong>
			und
			<strong>Echtzeit-Ankünfte</strong> von Bus und Tram im Raum <strong>Karlsruhe</strong> (KVV-Gebiet).
		</p>

		<p>
			Entstanden ist das Projekt aus Frust über bestehende (offizielle) Anzeigen, die häufig langsam
			reagieren, unzuverlässig sind, gelegentlich falsche Daten zeigen oder an weniger
			frequentierten Haltestellen ganz fehlen.
		</p>

		<p>
			<strong>Farben der Ankunftszeit:</strong>
			Ist die Ankunftszeit <span class="font-semibold text-red-600">rot</span>, ist die Bahn/der Bus
			zu spät. Ist sie <span class="font-semibold text-yellow-500">gelb</span>, liegen keine
			Echtzeitdaten vor, die Fahrt kommt also möglicherweise nicht.
			Ist sie <span class="font-semibold text-green-600">grün</span>, kommt die Bahn
			früher als geplant.
		</p>
	</header>

	<section aria-labelledby="features" class="space-y-3">
		<h2 id="features" class="text-lg font-semibold">Funktionen</h2>
		<ul class="list-disc space-y-1 pl-5">
			<li><strong>Haltestellensuche:</strong> Finde Haltestellen im KVV-Netzbereich.</li>
			<li>
				<strong>Echtzeit-Infos:</strong> Nächste Abfahrt/Ankunft, geplante Zeit, Minuten bis zur Fahrt
				und Verspätungen.
			</li>
			<li>
				<strong>Filter nach Steig/Gleis:</strong> Anzeige nach Steig-/Gleisnamen einschränken.
			</li>
			<li>
				<strong>Abfahrt/Ankunft umschalten:</strong> Wähle zwischen Abfahrten <em>von</em> oder
				Ankünften <em>zu</em> einer Haltestelle.
			</li>
			<li>
				<strong>Fokus auf Zuverlässigkeit:</strong> Optimiert für schnelle Ladezeiten und hohe Verfügbarkeit.
			</li>
		</ul>
	</section>

	<section aria-labelledby="usage" class="space-y-3">
		<h2 id="usage" class="text-lg font-semibold">So nutzt du die Seite</h2>
		<ol class="list-decimal space-y-1 pl-5">
			<li>Öffne die Website und suche oben nach deiner Haltestelle.</li>
			<li>Wähle bei Bedarf bestimmte Steige/Gleise über die Filter aus.</li>
			<li>
				Schalte zwischen <strong>Abfahrten</strong> und <strong>Ankünften</strong> um, je nachdem was
				du brauchst.
			</li>
			<li>Beachte ggf. Hinweise zu Verspätungen.</li>
		</ol>

		<p class="text-sm opacity-80">
			Tipp: Wenn du eine Haltestelle häufig nutzt, kannst du die Seite einfach als Lesezeichen
			speichern.
		</p>
	</section>

	<section aria-labelledby="reliability" class="space-y-3">
		<h2 id="reliability" class="text-lg font-semibold">Uptime & Zuverlässigkeit</h2>
		<p>
			Verfügbarkeit ist mir wichtig: Der Dienst wird kontinuierlich überwacht. Prüfungen laufen
			<strong>jede Minute</strong>, um Ausfälle schnell zu erkennen.
		</p>
